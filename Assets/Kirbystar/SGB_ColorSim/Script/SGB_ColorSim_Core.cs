    /// <summary>
    /// SGBカラーパスワードから、4色のカラーコードを取得する
    /// </summary>
    /// <param name="password">SGBパスワード、xxxx-xxxx-xxxx形式かxxxxxxxxxxxx形式。</param>
    /// <returns>コンマ区切りで4つのカラーコードが返りますが、失敗した場合は"*Err*"という文字列が返ります</returns>
    public string Pass2Color(string password)
    {
        Debug.Log(logPrefix + "SGBカラーシミュレータ Pass2Colorをはじめます");

        string resultColors = "*Err*";
        int[] c = new int[4];
        string[] resColor = new string[4];
        Debug.Log(logPrefix + "入力されたPasswordは" + password);

        //入力されたパスワードを処理
        if (Regex.IsMatch(password, @"^(\d{4}-\d{4}-\d{4}|\d{12})$"))
        {
            string pw = password;
            if (Regex.IsMatch(password, @"^(\d{4}-\d{4}-\d{4})$"))
            {
                Debug.Log(logPrefix + "xxxx-xxxx-xxxx形式なのでハイフンをとります");

                //xxxx-xxxx-xxxx型ならまずハイフンを取る
                password = password.Replace("-", "");
            }
            //4つに分ける
            c[0] = int.Parse(password.Substring(0, 3));
            c[1] = int.Parse(password.Substring(3, 3));
            c[2] = int.Parse(password.Substring(6, 3));
            c[3] = int.Parse(password.Substring(9, 3));
            Debug.Log(logPrefix + "4つにわけました" +
                "\nc0:" + c[0] +
                "\nc1:" + c[1] +
                "\nc2:" + c[2] +
                "\nc3:" + c[3]);
        }
        for (int cloop = 0; cloop <= 3; cloop++)
        {
            Debug.Log(logPrefix + cloop + "回目の処理をはじめます");
            int cc = DuplicateCheck(DeadCheck(c[cloop]));

            if (cc <= 511)
            {
                Debug.Log(logPrefix + "カラーコード" + cc + "は通常カラー");
                if (SGBNormalColorTable.TryGetValue(cc, out DataToken colorValue))
                {
                    resColor[cloop] = colorValue.ToString();
                    Debug.Log(logPrefix + "カラーコード" + cc + "の色は<color=" + resColor[cloop] + ">" + resColor[cloop] + "</color>");
                }
            }
            if (cc >= 512)
            {
                Debug.Log(logPrefix + "カラーコード" + cc + "はプリセットカラー");
                if (SGBPresetColorTable.TryGetValue(cc, out DataToken colorValue))
                {
                    resColor[cloop] = colorValue.ToString();
                    //プリセットカラーの場合は４つに分ける
                    string[] spritedColor = resColor[cloop].Split(',');
                    resColor[cloop] = spritedColor[cloop];
                    Debug.Log(logPrefix + "カラーコード" + cc + "の色は<color=" + resColor[cloop] + ">" + resColor[cloop] + "</color>");
                }
            }
        }
        //結果をまとめて返す
        resultColors = string.Join(",", resColor);
        Debug.Log(logPrefix + "最終的な結果は" + resultColors +
            "\n<color=" + resColor[0] + ">c0:" + resColor[0] + "</color>" +
            "\n<color=" + resColor[1] + ">c0:" + resColor[1] + "</color>" +
            "\n<color=" + resColor[2] + ">c0:" + resColor[2] + "</color>" +
            "\n<color=" + resColor[3] + ">c0:" + resColor[3] + "</color>");
        return resultColors;
    }

    /// <summary>
    /// カラーコードがDEAD扱いかチェックする
    /// </summary>
    /// <param name="ccode">SGBカラーコード</param>
    /// <returns>DEAD扱いのコードだった場合は704を返し、そうでない場合は入力されたコードがそのまま返る</returns>
    int DeadCheck(int ccode)
    {
        //Deadコード範囲
        //下限、上限、下限、上限、...
        int[] deadRanges = new int[] { 53, 63, 117, 127, 181, 191, 245, 255, 309, 319, 373, 383, 437, 447, 501, 511, 545, 575, 609, 639, 673, 703, 736, 767, 801, 831, 865, 895, 929, 959, 992, 999 };
        for (int i = 0; i < deadRanges.Length; i += 2)
        {
            if (deadRanges[i] <= ccode && ccode <= deadRanges[i + 1])
            {
                return 704;
            }
        }
        return ccode;
    }

    /// <summary>
    /// 重複パターンかチェックする
    /// </summary>
    /// <param name="ccode">SGBカラーコード</param>
    /// <returns>重複パターンだった場合は補正された値が返り、そうでない場合は入力されたコードがそのまま返る</returns>
    int DuplicateCheck(int ccode)
    {
        //重複コード
        int[] duplicateCodes = new int[] { 544, 608, 672, 800, 864, 928 };
        if (960 <= ccode && ccode <= 991) return ccode - 256;
        else if (duplicateCodes.Contains(ccode)) return ccode - 32;
        else return ccode;
    }
